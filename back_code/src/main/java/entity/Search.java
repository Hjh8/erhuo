package entity;

public class Search {
    private String image;
    private String id;
    private String name;
    private String genderOrMoney;
    private String dromOrSeller;

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGenderOrMoney() {
        return genderOrMoney;
    }

    public void setGenderOrMoney(String genderOrMoney) {
        this.genderOrMoney = genderOrMoney;
    }

    public String getDromOrSeller() {
        return dromOrSeller;
    }

    public void setDromOrSeller(String dromOrSeller) {
        this.dromOrSeller = dromOrSeller;
    }
}
